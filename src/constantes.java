
public class constantes {

	public static void main(String[] args) {
		
		// variable----------------------------------------
		int numero = 10;
		numero=15;
		System.out.println("el numero variable es "+numero);
		
		// constante---------------------------------------
		final int numer = 10;
		System.out.println("el numero constante es "+numer);
		  
	}

}
